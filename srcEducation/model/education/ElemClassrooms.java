package model.education;

/**
 *
 * @author Shermaine
 */
public class ElemClassrooms {
    private String gradeLevel;
    private int classroomCount;

    /**
     * @return the gradeLevel
     */
    public String getGradeLevel() {
        return gradeLevel;
    }

    /**
     * @param gradeLevel the gradeLevel to set
     */
    public void setGradeLevel(String gradeLevel) {
        this.gradeLevel = gradeLevel;
    }

    /**
     * @return the classroomCount
     */
    public int getClassroomCount() {
        return classroomCount;
    }

    /**
     * @param classroomCount the classroomCount to set
     */
    public void setClassroomCount(int classroomCount) {
        this.classroomCount = classroomCount;
    }
}
